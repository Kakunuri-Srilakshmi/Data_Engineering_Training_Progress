        "# Cloud Computing\n",
        "\n",
        "\n",
        "\n",
        "## Introduction  \n",
        "Cloud computing is the delivery of IT resources such as servers, storage, databases, networking, and software over the internet.  \n",
        "It offers **on-demand service, pay-as-you-go pricing, scalability, global access, and reliability**.  \n",
        "\n",
        "\n",
        "## On-Premises Hosting (Traditional IT Model)  \n",
        "\n",
        "### Example: TaxSmile – IT Returns Portal (On-Premises Solution)  \n",
        "- In February, the portal was launched with **2 physical servers**.  \n",
        "- From April to August, additional **3 servers** were required for load and stress testing.  \n",
        "\n",
        "**Steps Involved**  \n",
        "- Approvals from stakeholders like Directors, CEO, and COO.  \n",
        "- Servers procured from a vendor in Germany, which took nearly 2 weeks.  \n",
        "- Network engineers worked **24/7**, organized into **3 main teams plus 1 backup team** (incurring CTC).  \n",
        "- Additional costs for **power supply** and **room rent** to host the servers.  \n",
        "\n",
        "**Challenges**  \n",
        "- Very slow process (weeks for procurement).  \n",
        "- High upfront investment in hardware and resources.  \n",
        "- Complex maintenance of network, power, cooling, and staff.  \n",
        "- Scalability was limited.  \n",
        "\n",
        "\n",
        "\n",
        "## Cloud Hosting (Modern IT Model)  \n",
        "\n",
        "### Example: TaxSmile – Cloud Solution  \n",
        "- In February, the system was deployed using **2 Virtual Machines (VMs)** instantly.  \n",
        "- Application services were configured on **2 servers** virtually without procurement delays.  \n",
        "- Scaling was elastic, meaning resources automatically increased or decreased based on demand.  \n",
        "\n",
        "**Benefits**  \n",
        "- Provisioning within minutes instead of weeks.  \n",
        "- Pay only for the resources used.  \n",
        "- No physical maintenance required.  \n",
        "- High availability, reliability, and global access.  \n",
        "\n",
        "\n",
        "\n",
        "## Elastic Scaling  \n",
        "- Elasticity allows cloud resources to automatically expand or shrink depending on demand.  \n",
        "- **Horizontal Scaling (Scale Out/In):** Adding or removing servers.  \n",
        "  - Example: Adding 5 VMs during peak tax-filing season.  \n",
        "- **Vertical Scaling (Scale Up/Down):** Increasing or decreasing CPU, RAM, or storage of an existing server.  \n",
        "\n",
        "\n",
        "\n",
        "## Virtual Machines (VMs)  \n",
        "- A VM is a software-based environment that behaves like a physical computer.  \n",
        "- Multiple VMs can run on the same physical server.  \n",
        "- Advantages include isolation, easy migration, and cost-effectiveness.  \n",
        "\n",
        "\n",
        "\n",
        "## Hypervisors (Virtualization Layer)  \n",
        "- A hypervisor enables multiple VMs to run on a single server by allocating CPU, RAM, and storage.  \n",
        "- **Type 1 (Bare Metal):** Runs directly on hardware (examples: VMware ESXi, Microsoft Hyper-V).  \n",
        "- **Type 2 (Hosted):** Runs on top of an OS (examples: VirtualBox, VMware Workstation).  \n",
        "\n",
        "\n",
        "\n",
        "## Microsoft Azure Cloud Architecture  \n",
        "- **Data Centers** across the world host cloud services.  \n",
        "- **Racks** contain servers and storage devices.  \n",
        "- **Servers** run applications, VMs, and databases.  \n",
        "- **Network Switches** provide connectivity.  \n",
        "- **Fabric Controller** manages servers, load balancing, and failover.  \n",
        "- **Orchestrator** automates provisioning and scaling.  \n",
        "- **Web API** allows developers to access Azure services.  \n",
        "- **Virtualization** enables multiple VMs on a single server.  \n",
        "\n",
        "\n",
        "\n",
        "## Cloud Service Models  \n",
        "- **Infrastructure as a Service (IaaS):** Provides virtualized infrastructure like servers, networking, and storage.  \n",
        "  - Examples: AWS EC2, Azure VMs.  \n",
        "- **Platform as a Service (PaaS):** Provides a platform for developers to build and run applications.  \n",
        "  - Examples: Azure App Services, Google App Engine.  \n",
        "- **Software as a Service (SaaS):** Provides ready-to-use applications over the cloud.  \n",
        "  - Examples: Microsoft 365, Salesforce, Gmail.  \n",
        "\n",
        "\n",
        "## Summary  \n",
        "On-premises hosting involves high cost, delays, and limited scalability.  \n",
        "Cloud hosting provides faster deployment, cost savings, elastic scaling, and global access.  \n",
